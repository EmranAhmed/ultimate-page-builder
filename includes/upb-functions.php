    function upb_get_edit_link( $post = 0 ) {
        if ( is_admin() ) {
            return esc_url( add_query_arg( 'upb', '1', wp_get_shortlink( $post ) ) );
        } else {
            return esc_url( add_query_arg( 'upb', '1', get_permalink( $post ) ) );
        }
    }

